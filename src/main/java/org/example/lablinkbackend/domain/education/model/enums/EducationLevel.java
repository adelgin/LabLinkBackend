package org.example.lablinkbackend.domain.education.model.enums;

/**
 * Перечисление уровней образования в соответствии с системой образования РФ
 * и международными стандартами.
 */
public enum EducationLevel {

    // --- Школьное образование ---

    /** Основное общее образование (9 классов) */
    BASIC_GENERAL,

    /** Среднее общее образование (11 классов, аттестат) */
    SECONDARY_GENERAL,

    // --- Среднее профессиональное образование (Колледжи, Техникумы, Училища) ---

    /** Профессиональное обучение (курсы рабочих профессий) */
    VOCATIONAL_TRAINING,

    /** Подготовка специалистов среднего звена (классический диплом колледжа) */
    MIDDLE_MANAGEMENT_LEVEL,

    /** Подготовка квалифицированных рабочих/служащих */
    SKILLED_WORKER,

    // --- Высшее образование ---

    /** Бакалавриат */
    BACHELOR,

    /** Специалитет (5 лет) */
    SPECIALIST_DEGREE,

    /** Магистратура */
    MASTER,

    // --- Подготовка кадров высшей квалификации ---

    /** Аспирантура */
    POSTGRADUATE_STUDIES,

    /** Ординатура (для медицинских специальностей) */
    RESIDENCY,

    /** Адъюнктура / Ассистентура-стажировка */
    ASSISTANTSHIP,

    // --- Ученые степени ---

    /** Кандидат наук (эквивалент PhD) */
    CANDIDATE_OF_SCIENCES,

    /** Доктор наук */
    DOCTOR_OF_SCIENCES,

    // --- Дополнительное образование ---

    /** Дополнительное профессиональное образование (повышение квалификации, переподготовка) */
    ADDITIONAL_PROFESSIONAL
}